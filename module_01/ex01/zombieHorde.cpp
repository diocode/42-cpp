#include "Zombie.hpp"

Zombie*	zombieHorde(int N, std::string name)
{
	Zombie*	horde;

	if (!N || name.empty())
		return 0;
	horde = new Zombie[N];
	for (int i = 0; i < N; i++)
		new	(&horde[i]) Zombie(name);

	return horde;
}
